    public void RTable() {

        try {
            pst = con.prepareStatement("SELECT * FROM Pesanan");
            ResultSet rs = pst.executeQuery();

            ResultSetMetaData rsm = rs.getMetaData();
            int count = rsm.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= count; i++) {
                    v2.add(rs.getString("No_Pesanan"));
                    v2.add(rs.getString("Username"));
                    v2.add(rs.getString("Tanggal_Pesanan"));
                    v2.add(rs.getString("Alamat_Tujuan"));
                    v2.add(rs.getString("Total"));
                    v2.add(rs.getString("Id_Driver"));

                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Restoran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
